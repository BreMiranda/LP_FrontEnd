/* Escreva um programa que declare duas variaveis
numéricas e realize as quatro operações matemáticas básicas
(+, -, *, /). Imprima os resultados no console */

function soma(a, b){
    return a+b
}
function subtracao(a,b){
    return a-b
}
function mult(a,b){
    return a*b
}
function divi(a,b){
    return a / b
}

console.log("a soma dos número é "+soma(2,2));
console.log("a subtracao dos número é "+subtracao(2,2));
console.log("a multiplicacao dos número é "+mult(2,2));
console.log("a divisao dos número é "+divi(2,2));